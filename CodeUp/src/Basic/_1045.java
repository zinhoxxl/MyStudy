package Basic;

import java.util.Scanner;

public class _1045 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
	    int x = sc.nextInt();
	    int y = sc.nextInt();
	    System.out.println(x+y);
	    System.out.println(x-y);
	    System.out.println(x*y);
	    System.out.println(x/y);
	    System.out.println(x%y);
	    System.out.printf("%.2f",(double) x/y);
	}
}
